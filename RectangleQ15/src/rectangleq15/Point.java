package rectangleq15;

public class Point {
    
    private int pointx;
    private int pointy;
    
    public Point (int x, int y){
        pointx = x;
        pointy = y;
        
    }
    
    public int getPointX () {
        return pointx ;
    
    }
    public int getPointY () {
        return pointy ;
  
    }
}

